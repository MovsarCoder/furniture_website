# –ö–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ nginx
if [ -f "nginx/nginx.conf" ]; then
    cp nginx/nginx.conf /etc/nginx/sites-available/bmass
    echo "‚úÖ –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è nginx —Å–∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∞"
else
    echo "‚ùå –§–∞–π–ª nginx/nginx.conf –Ω–µ –Ω–∞–π–¥–µ–Ω!"
    exit 1
fi

# –°–æ–∑–¥–∞–Ω–∏–µ —Å–∏–º–ª–∏–Ω–∫–∞
