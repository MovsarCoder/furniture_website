    echo "‚ùå –û—à–∏–±–∫–∞ –≤ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ nginx!"
    exit 1
fi

echo "üìÅ –°–æ–∑–¥–∞–µ–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏..."

# –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–π
mkdir -p staticfiles media

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –≤–ª–∞–¥–µ–ª—å—Ü–∞ (–ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, –∫–æ—Ç–æ—Ä—ã–π –∑–∞–ø—É—Å—Ç–∏–ª sudo)
if [ -n "$SUDO_USER" ]; then
    OWNER="$SUDO_USER"
else
    OWNER="$(whoami)"
fi

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∞–≤
chown -R "$OWNER:www-data" "$PROJECT_DIR"
chmod -R 755 "$PROJECT_DIR"
chmod -R 775 staticfiles media  # –ü—Ä–∞–≤–∞ –Ω–∞ –∑–∞–ø–∏—Å—å –¥–ª—è –º–µ–¥–∏–∞
echo "‚úÖ –ü—Ä–∞–≤–∞ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã"

echo "üê≥ –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º Docker Compose..."

# –ó–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä—É–µ–º –ø–æ—Ä—Ç 8000 –≤ docker-compose.yml –¥–ª—è –ø—Ä–æ–¥–∞–∫—à–µ–Ω–∞
# (nginx –±—É–¥–µ—Ç –ø—Ä–æ–∫—Å–∏—Ä–æ–≤–∞—Ç—å –∑–∞–ø—Ä–æ—Å—ã)
if grep -q "^\s*-\s*\"8000:8000\"" docker-compose.yml; then
    sed -i 's/^\(.*- "8000:8000"\)/#\1  # –ó–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–æ –¥–ª—è –ø—Ä–æ–¥–∞–∫—à–µ–Ω–∞ —Å nginx/' docker-compose.yml
    echo "‚úÖ –ü–æ—Ä—Ç 8000 –∑–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä–æ–≤–∞–Ω –≤ docker-compose.yml"
fi

echo "üöÄ –ó–∞–ø—É—Å–∫–∞–µ–º Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã..."

# –ó–∞–ø—É—Å–∫ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤ (–æ—Ç –∏–º–µ–Ω–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, –Ω–µ root)
if [ -n "$SUDO_USER" ]; then
    sudo -u "$SUDO_USER" docker-compose up -d --build
else
    docker-compose up -d --build
fi

echo "‚è≥ –ñ–¥–µ–º –≥–æ—Ç–æ–≤–Ω–æ—Å—Ç–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤..."
